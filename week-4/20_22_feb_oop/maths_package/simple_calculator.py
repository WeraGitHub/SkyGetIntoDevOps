# create 4 functions: add, subtract, multiply and divide

def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


if __name__ == '__main__':
    print(add(10, 6))
    print(subtract(10, 6))
    print(multiply(10, 6))
    print(divide(10, 6))
